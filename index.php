                                <article class="gdg">
                                    <header class="clearfix">
                                        <h1>About Google Developer Groups (GDG)</h1>
                                        <img src="images/gdg.jpg" width="180">
                                    </header>
                                    <p>Google Developer Groups (GDGs) are for developers who are interested in Google's developer technology;
                                        everything from the <a target="_blank" href="http://code.google.com/android">Android</a>, <a href="http://developers.google.com/appengine/" target="_blank">App Engine</a>, and <a href="http://developers.google.com/chrome/">Google Chrome</a> platforms, to product APIs like the <a target="_blank" href="http://developers.google.com/maps/">Maps API</a>, <a target="_blank" href="http://developers.google.com/youtube/">YouTube API</a> and <a target="_blank" href="http://developers.google.com/google-apps/calendar/">Google Calendar API</a>.</p>
                                    <p>The GDG can take many forms — from just a few people getting together to watch the latest developer video, to large gatherings with demos and tech talks, to events like code sprints and hackathons. However, at the core, GDGs are focused on developers and technical content, and the core audience are  developers.</p>


                                    <p>There are over 7 GDG chapters in Kenya including; GDG Kampala,
                                        GDG Mombasa,
                                        GDG Nairobi,
                                        GDG Kimathi University,
										GDG Kisumu,
                                        GDG MMUST
                                        GDG JKUAT, &
                                        GDG Strathmore </p>
                                </article>

                                <br />

                                <article class="gdg">
                                    <header class="clearfix">
                                        <h1>About Google Business Groups(GBG)</h1>
                                        <img src="images/gbg.jpg" width="160">
                                    </header>
                                    <p>Google Business Groups are a global network of business professionals interested in learning, sharing knowledge,
                                        exchanging experiences and enhancing work efficiency through the tools and products offered by Google.
                                        GBG is represented by local groups (GBG chapters) which organize social gatherings, workshops, online hangouts and conferences

                                    </p>


                                    <p>There is one GBG chapter in Kenya. i.e GBG NAIROBI which is a
                                        community driven forum, chapter members meet and learn from fellow members
                                        (experienced professionals) passionate to help local businesses grow with the help of Google technologies.
                                        Many more chapters are yet to come.
                                    </p>




                                </article>

                                <br />

                                <article class="gsa">
                                    <header class="clearfix">
                                        <h1>About Google Student Ambassadors (GSA)</h1>
                                        <img src="images/gsa.jpg" width="180">
                                    </header>
                                    <p>
                                        The Google Student Ambassador Program is an opportunity for students to act as liaisons between Google and their universities. These ambassadors:
                                    <ul>Learn about innovative Google products and programs.</ul>
                                    <ul>Plan and host fun events on campus.</ul>
                                    <ul>Act as a campus contact for Google teams.</ul>
                                    <ul>Help Google better understand each university’s culture</ul>
                                    </p>



                                    <p>Majority of the universities in Kenya have Google Student Ambassadors. For example:
                                        University of Nairobi,
                                        Jomo Kenyatta University of Technology,
                                        Kenyatta University,
                                        Maseno University,
                                        Kimathi University,
                                        Moi University,
                                        Kenya Methodist University
                                        Strathmore University &
                                        Egerton University
                                    </p>
